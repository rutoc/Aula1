public class Person {

    private String name;
    private int cpf;




    final public String getName() {
        return name;
    }

    final public void setName(String name) {
        this.name = name;
    }

    final public int getCpf() {
        return cpf;
    }

    final public void setCpf(int cpf) {
        this.cpf = cpf;
    }
}
